    var a: Int=25
    var b: Float=1.5f
    var c: Char='M'
    var d: String="Hello World!!"
    var e: Boolean=true
    val f: Double=94.95
    val g: Long=332454647L
    var h: Byte=20
    var i: Short=-5
    println("21012011049_Meha Bhatt")
